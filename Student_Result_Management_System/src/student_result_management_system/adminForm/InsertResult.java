    private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButtonActionPerformed
        // TODO add your handling code here:
        var physics = (String)physicsTextField.getText();
        var maths = (String)mathsTextField.getText();
        var rollNo = rollTextField.getText();
        var em = emTextField.getText();
        var dbms = dbmsTextField.getText();
        var os = osTextField.getText();

        // JDBC driver name and database URL
        final String JDBC_DRIVER = "org.postgresql.Driver";
        final String DB_URL = "jdbc:postgresql://localhost:5432/student_result_management_system";

        // Database credentials
        final String USER = "postgres";
        final String PASS = "postgres";

        Connection con = null;
        ResultSet rs = null;
        Statement st = null;

        try {
            Class.forName(JDBC_DRIVER);
            con = DriverManager.getConnection(DB_URL, USER,PASS);

            st = con.createStatement();
            rs = st.executeQuery("SELECT * FROM student where rollno = '" +rollNo+ "'");
            
            if(rs.next()){
                
                var stName = rs.getString("fullname");
                st.execute("INSERT INTO result(rollno, physics, maths, em, dbms, os) values('"+rollNo+"', '"+physics+"', '"+maths+"', '"+em+"','"+dbms+"', '"+os+"')");
                JOptionPane.showMessageDialog(null, "Marks for student "+stName+" Submitted Successfully !!!");
                setVisible(false);
                new InsertResult().setVisible(true);
                
            }  else {
                JOptionPane.showMessageDialog(null, "Student is not Registered !!!");
            }          
                        
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.toString());
        } finally {

            // Close the connection
            try {
                if (rs != null) rs.close();
                if (st != null) st.close();
                if (con != null) con.close();
            } catch (SQLException se) {
                JOptionPane.showMessageDialog(null, se.toString());
            }
        }
    }//GEN-LAST:event_submitButtonActionPerformed

