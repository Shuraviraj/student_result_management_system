    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        var username = userNameTextField.getText();
        var pass = jPasswordField.getText();
        
        // JDBC driver name and database URL
        final String JDBC_DRIVER = "org.postgresql.Driver";
        final String DB_URL = "jdbc:postgresql://localhost:5432/student_result_management_system";

        // Database credentials
        final String USER = "postgres";
        final String PASS = "postgres";

        Connection con = null;
        Statement st = null;
        ResultSet rs = null;

        try {
            Class.forName(JDBC_DRIVER);
            con = DriverManager.getConnection(DB_URL, USER,PASS);

            st = con.createStatement();
            rs = st.executeQuery("SELECT * FROM admin where username = '" +username+ "'");
            
            if(rs.next()) {
                JOptionPane.showMessageDialog(null, "Admin with name : "+username+" Already Present try with different name");
            } else {
                st.execute("INSERT INTO admin(username, pass) values('"+username+"', '"+pass+"')");
                JOptionPane.showMessageDialog(null, "Admin with name : "+username+" created Successfully !!!");
            }
                                        
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.toString());
        } finally {

            // Close the connection
            try {
                if (st != null) st.close();
                if (con != null) con.close();
            } catch (SQLException se) {
                JOptionPane.showMessageDialog(null, se.toString());
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

