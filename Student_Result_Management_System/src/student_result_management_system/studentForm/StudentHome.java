    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        
        var roll = rollTextField.getText();
        
        // JDBC driver name and database URL
        final String JDBC_DRIVER = "org.postgresql.Driver";
        final String DB_URL = "jdbc:postgresql://localhost:5432/student_result_management_system";

        // Database credentials
        final String USER = "postgres";
        final String PASS = "postgres";

        Connection con = null;
        ResultSet rs = null;
        Statement st = null;
        
        try {
            Class.forName(JDBC_DRIVER);
            con = DriverManager.getConnection(DB_URL, USER,PASS);

            st = con.createStatement();
            rs = st.executeQuery("SELECT * FROM student INNER JOIN result on student.rollno = result.rollno where student.rollno = '" + roll + "'" );
            
            if(rs.next()) {
                setVisible(false);
                new StudentIndex(roll).setVisible(true);
            } else {
                JOptionPane.showMessageDialog(null, "No Student is available for given roll Number :" + roll);
            }
                        
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.toString());
        } finally {

            // Close the connection
            try {
                if (rs != null) rs.close();
                if (st != null) st.close();
                if (con != null) con.close();
            } catch (SQLException se) {
                JOptionPane.showMessageDialog(null, se.toString());
            }
        }
    }//GEN-LAST:event_searchButtonActionPerformed

